import SpotifyWebApi from "spotify-web-api-node";

export default async function handler(req, res) {
  const { mood, genre, tracks, name, token } = req.body;

  if (!token) {
    return res.status(401).json({ error: "No token provided" });
  }

  try {
    const spotify = new SpotifyWebApi();
    spotify.setAccessToken(token);

    // 🔎 SEARCH TRACKS
    const searchQuery = `${genre || ""} ${mood || ""}`;

    const result = await spotify.searchTracks(searchQuery, {
      limit: Number(tracks) || 20,
    });

    const trackUris = result.body.tracks.items.map((t) => t.uri);

    if (trackUris.length === 0) {
      return res.status(404).json({ error: "No tracks found" });
    }

    // 👤 USER
    const user = await spotify.getMe();

    // 📀 CREATE PLAYLIST
    const playlist = await spotify.createPlaylist(
      user.body.id,
      name || "Photo Playlist 🎵",
      {
        description: "Generated by Photo to Playlist app",
        public: false,
      }
    );

    // ➕ ADD TRACKS (max 100 per request)
    await spotify.addTracksToPlaylist(playlist.body.id, trackUris);

    return res.status(200).json({
      url: playlist.body.external_urls.spotify,
      id: playlist.body.id,
    });
  } catch (error) {
    console.error("PLAYLIST ERROR:", error.response?.body || error.message);

    return res.status(500).json({
      error: "Playlist creation failed",
      details: error.response?.body || error.message,
    });
  }
}